package br.com.zup;

//Faça um programa que mostra na tela os números de 100 a 1.

public class Lista004Ex2 {
    public static void main(String[] args) {

        System.out.println( "Números de 100 a 1: " );

        // Decrementar
        int contador = 100;
        while (contador > 0) {
            System.out.println(contador);
            contador --;

        }

    }
}
